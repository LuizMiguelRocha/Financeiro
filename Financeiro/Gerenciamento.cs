namespace Financeiro;
public class Gerenciamento
{
   string Nome;
   Int64 Valor;

   public void SetNome (string Nome)
   {
    this.Nome = nome;
   }
   public string GetNome()
   {
    return nome;
   }

   public void SetValor (string Valor)
   {
      this.Valor = valor;
   }
   public string GetValor()
   {
      return valor;
   }
}